#Working with while loop statements
'''
WAP to print your name for 5 times using looping statements or Iterative statements(while/for)
'''
i=0 #Initializing the loop variable with the value 1
while i<=5:
    print('Tahoora using while loop')
    i=i+1

print('End of while loop and start of for loop')

for i in range(0,5):   #range function will generate integers from 0 till 4
    print('Tahoora using for loop')
